from tqdm import tqdm
import numpy as np

word2vec_path = r"E:\MyFiles\WorkSpace\NLP\Word2Vec\numberbatch-en.txt"


def read_word2vec(path=word2vec_path):
    word2vec_matrix = {}
    with open(path, encoding='utf-8') as f:
        print("reading the word2vec matrix")

        for line in tqdm(f):
            values = line.split(' ')
            word = values[0]
            vec = np.asarray(values[1:], dtype='float32')
            word2vec_matrix[word] = vec
        print("Reading Completed !")
        return word2vec_matrix


